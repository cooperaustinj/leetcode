/**
 * @param {string} s
 * @return {boolean}
 */
var isPalindrome = function (s) {
    let head = 0;
    let tail = s.length - 1;
    while (head < tail) {
        const hc = validChars[s[head]];
        const tc = validChars[s[tail]];
        if(!hc) ++head;
        else if(!tc) --tail;
        else if(hc !== tc) return false;
        else {
            ++head;
            --tail;
        }
    }
    return true;
};
